S SEGMENT PARA PUBLIC 'CODE'
ASSUME CS: S
ORG 1000H

START:
MOV AL, 80H
OUT 1FH,AL

XOR BX,BX
MOV BL,3

MOV SI,OFFSET DATA
L1:
MOV AL,BYTE PTR CS:[SI]
OUT 19H,AL

;FOR DELAY:
MOV CX, 0FFFFH
L2: LOOP L2
MOV CX, 0FFFFH
L3: LOOP L3
MOV CX, 0FFFFH
L4: LOOP L4
MOV CX, 0FFFFH
L5: LOOP L5

INC SI
DEC BL
CMP BL,0
JE BREAKLOOP
JMP L1

DATA:
DB 088H		;Displaying A
DB 0F9H		;Displaying 1
DB 0A4H 	;Displaying 2

BREAKLOOP:
S ENDS
END START